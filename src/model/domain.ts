/*
Types
 */
export type ProgramRoundStrategy =
    | 'noRound'
    | 'threeDigits'
    | 'twoDigits'
    | 'oneDigit'
    | 'fiveDigits'
    | 'firstMeaningDigit';
export type SegmentType =
    | 'md'
    | 'computational'
    | 'latex'
    | 'asciimath'
    | 'empty';

// Viewer/Project display mode for result rendering
export type ProjectType = 'markdown' | 'latex';
export type StatementType =
    | 'assignment'
    | 'calculation'
    | 'file'
    | 'table'
    | 'plot'
    | 'latex'
    | 'no_result';
export type PlotType = 'line' | 'scatter' | 'histogram' | 'dotted';
export type OpenParams = 'login' | 'ai' | 'latex' | 'markdown';

/*
Domain
 */
export interface UserInfo {
    email: string;
    id: number;
    isAuthenticated: boolean;
    tokens: number;
}

export interface LabkeeperFile {
    autogenerated: boolean;
    fileName: string;
    url: string;
}

export interface Segment {
    type: SegmentType;
    parameters: {
        visible?: boolean;
        hideAssignment?: boolean;
        hideAssignmentWithValues?: boolean;
        hideValue?: boolean;
        hideGeneralFormula?: boolean;
        hideInflAssignment?: boolean;
        hideInflAssignmentWithValues?: boolean;
        hideInfl?: boolean;
    };
    text: string;
}

export interface CompileSuccessResult {
    segments: OutputSegment[];
}

export interface OutputSegment {
    type: SegmentType;
}

export interface ComputationalOutputSegment extends OutputSegment {
    statements: Statement[];
    type: 'computational';
}

export interface TextOutputSegment extends OutputSegment {
    text: string;
    type: 'md' | 'latex' | 'asciimath';
}

export interface Statement {
    type: StatementType;
}

export interface ProgramParameters {
    roundStrategy: ProgramRoundStrategy;
}

export interface Program {
    segments: Segment[];
    parameters: ProgramParameters;
}

export interface Project {
    projectId: string;
    userId: number;
    title: string;
    lastModified: string;
    program: Program;
    isPublic: boolean;
    projectType: ProjectType;
}

export interface ProjectShort {
    projectId: string;
    userId: number;
    title: string;
    lastModified: string;
}

