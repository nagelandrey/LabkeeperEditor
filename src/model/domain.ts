    // TOKENIZER
    CODE_NO_END_QUOTES = 100,
    UNKNOWN_SYMBOL = 102,
    QUOTA_EXCEEDED = 103,

    // PARSER
    OPERATOR_EXPECTED = 202,
    NUMBER_EXPECTED = 203,
    NAME_EXPECTED = 204,
    NAME_RESERVED = 205,

    // INTERPRETER
    NO_SUCH_VARIABLE = 301,
    ARITHMETIC_ERROR = 305,
    CANCELED = 306,
    NOT_ENOUGH_WORKERS = 307,
    FILE_USAGE_NOT_ALLOWED = 308,
    TOO_MUCH_FILES = 310,
    INCORRECT_INFL_DEFINITION_ARRAY_SIZE = 311,
    INCORRECT_INFL_DEFINITION_VALUE_WITH_INFL = 312,
    ARRAY_HAVE_ONLY_ZERO_VALUES = 313,

    // FUNCTIONS
    INCORRECT_ARGUMENT_SIZE = 401,
    INCORRECT_ARGUMENTS_COUNT = 402,
